package Day_01;

public class SwapNumbers {
    public static void main(String[ ] args){
        int a = 10;
        int b = 20;
        int c;
        System.out.println("Before swap a = "+ a+ " , " +"b = "+b);
        c= a;
        a=b;
        b=c;
        System.out.println("After swap  a=  "+ a+ " , b = " +b);

    }
}
